#!/usr/bin/env python3
"""
@description arithmetic operations on python

the following it the avaible arithmetic operation on python
Symbol      mean
(+)         Addition
(-)         Subtraction
(*)         multiplication
(/)         Division
"""

# variable y holds 10 and variable x hold 5
x = 10
y = 5

# Addition
print(f"Addition, x(10) + y(5) = {x + y}")

# Subtraction
print(f"Subtraction, x(10) - y(5) = {x - y}")

# Multiplication
print(f"Subtraction, x(10) * y(5) = {x * y}")

# Division
print(f"Division, x(10) / y(5) = {x / y}")

# Module
print(f"Modulus, x(10) % y(5) = {x % y}")

# Exponent
print(f"Exponent, x(10) ** y(5) = {x ** y}")

# Floor Division
print(f"Floor Division, x(10) // y(5) = {x // y}")


